#include <iostream>

using namespace std;

int main()
{
	
	//1.1)
	int a;
	// a = 2 + 2
	cout << a << endl;

	//1.2)
	int a1 = 0;
	//a + 1 de 3 formas diferentes
	cout << a1 << endl;

	//2.1)
	int b;
	// int b = 2 - 2
	cout << b << endl;

	//2.2)
	int b1 = 1;
	//b1 - 1 de 3 formas diferentes
	cout << b1 << endl;

	//3.1)
	int c;
	// c = 1 * 2
	cout << c << endl;

	//3.2)
	int c1 = 1;
	// c1 * 2 de 2 formas diferentes
	cout << c1 << endl;

	//4.1)
	int d;
	// d = 4 / 2
	cout << d << endl;

	// 4.2)
	int d1;
	// d1 = 5 / 2
	cout << d1 << endl;

	// 4.3)
	float d2;
	// d2 = 5 / 2
	cout << d2 << endl;

	// 4.3)
	float d3;
	// d3 = 5.0 / 2
	cout << d3 << endl;

	// 4.4)
	int d4;
	// d4 = 8 / 3 trunca sempre
	cout << d4 << endl;
	
	// 5.1)
	int e;
	// e = 30 % 4
	cout << e << endl;


	return 0;
}


